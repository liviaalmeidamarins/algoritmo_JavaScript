let mes = prompt('Digite um número: ')

mes = parseInt(mes)

switch(mes){
    case 1: 
    console.log('Janeiro')
    break

    case 2: 
    console.log('Feverreiro')
    break

    case 3: 
    console.log('Março')
    break

    case 4: 
    console.log('Abril')
    break

    default:
        console.log('Número não encontrado')
            break;
    
    

    }
